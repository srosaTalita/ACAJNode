function soma(n1, n2){
    let soma = n1 + n2;
    return soma;
}

function sub(n1, n2){
    let sub = n1 - n2;
    return sub;
}

function mult(n1, n2){
    let mult = n1 * n2;
    return mult;
}

function div(n1, n2){
    let div = n1 / n2;
    return div;
}

console.log(soma(8, 2))
console.log(sub(8, 2))
console.log(mult(8, 2))
console.log(div(8, 2))